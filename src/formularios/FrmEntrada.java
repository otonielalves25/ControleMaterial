            Entrada ent = entradaDao.consultaPorID(Entrada.class, idEntrada);
            txtCodigo.setText(ent.getIdEntrada() + "");
            txtID.setText(ent.getProduto().getIdProduto() + "");
            txtMaterial.setText(ent.getProduto().getNomeProduto());
            utilidades.ManipulaImagem.exibirImagemNoLabel(ent.getProduto().getFoto(), paneFoto);
            cboFornecedor.getModel().setSelectedItem(ent.getFornecedor());
            txtQuantidade.setText(ent.getQuantidade() + "");
