    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        // TODO add your handling code here:
        // Validando os campos obrigatórios
        if (ValidarCampos.validarCampo(txtMaterial, "material")) {
            return;
        }
        if (ValidarCampos.validarCampo(cboFornecedor, "fornecedor")) {
            return;
        }
        if (ValidarCampos.validarCampo(txtQuantidade, "quantidade")) {
            return;
        }
        if (ValidarCampos.validarCampo(txtObservacao, "observacao")) {
            return;
        }
        //  Fim - Validando os campos obrigatórios

        Entrada entrada = new Entrada();
        Produto produto = produtoDao.consultaPorID(Produto.class, Integer.parseInt(txtID.getText()));
        Fornecedor fornecedor = (Fornecedor) cboFornecedor.getSelectedItem();
        entrada.setDataEntrada(new SimpleDateFormat("yyyy-MM-dd").format(new Date()));
        entrada.setQuantidade(Integer.parseInt(txtQuantidade.getText()));
        entrada.setFornecedor(fornecedor);
        entrada.setProduto(produto);
        entrada.setUsuario(new Usuario(10));
        entrada.setObservacao(txtObservacao.getText());

        if (novo) {

            entradaDao.salvar(entrada);
            adicionaQuantidade(Integer.parseInt(txtQuantidade.getText()), produto);
            JOptionPane.showMessageDialog(this, "Cadatrado com Sucesso !!!", null, JOptionPane.INFORMATION_MESSAGE);

        } else {

            entrada.setIdEntrada(Integer.parseInt(txtCodigo.getText()));
            entradaDao.update(entrada);
            JOptionPane.showMessageDialog(this, "Alterado com Sucesso !!!", null, JOptionPane.INFORMATION_MESSAGE);
            this.dispose();

        }

        botaoInicial();
        novo = false;
        habilitado(false);
        limparTudo();


    }//GEN-LAST:event_btnSalvarActionPerformed

