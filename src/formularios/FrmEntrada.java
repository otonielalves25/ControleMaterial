    //BOTAO INICIAR*************************************************************
    private void botaoInicial() {
        btnNovo.setEnabled(true);
        btnCancelar.setEnabled(true);
        btnExcluir.setEnabled(false);
        btnCancelar.setEnabled(true);
        btnAlterar.setEnabled(false);
        btnSalvar.setEnabled(false);
    }

    //BOTAO NOVO*************************************************************
    private void botaoNovo() {
        btnNovo.setEnabled(false);
        btnCancelar.setEnabled(true);
        btnExcluir.setEnabled(false);
        btnCancelar.setEnabled(true);
        btnAlterar.setEnabled(false);
        btnSalvar.setEnabled(true);
    }

    //LIMPAR******************************************************************
    private void limparTudo() {
        txtCodigo.setText("");
        txtMaterial.setText("");
        txtID.setText("");
        cboFornecedor.setSelectedItem("Selecione...");
        txtQuantidade.setText("");
        txtObservacao.setText("");
        paneFoto.setText("IMAGEM");
        paneFoto.setIcon(new ImageIcon());

    }

    //LIMPAR******************************************************************
    private void habilitado(boolean y) {
        txtMaterial.setEnabled(y);
        cboFornecedor.setEnabled(y);
        txtQuantidade.setEnabled(y);
        txtObservacao.setEnabled(y);
        btnBusca.setEnabled(y);

    }


