    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        // TODO add your handling code here:

        if (this.txtCodigo.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Selecione uma Categoria para Excluír.", null, JOptionPane.ERROR_MESSAGE);

        } else {

            int resposta;
            resposta = JOptionPane.showConfirmDialog(this, "Deseja realmente excluir?", "Escolha um", JOptionPane.YES_NO_OPTION);

            if (resposta == JOptionPane.YES_OPTION) {

                /**
                 * VERIFICA QUAL PEDIDO PARA DIMINUIR NO BANCO /////////////////
                 */
                int id = Integer.parseInt(txtID.getText());
                Produto p = produtoDao.consultaPorID(Produto.class, id);
                int qtde = Integer.parseInt(txtQuantidade.getText());

                if (p.getQuantidade() < qtde) {
                    JOptionPane.showMessageDialog(this, "Quantidade não pode ser excluída.", null, JOptionPane.ERROR_MESSAGE);
                    return;
                }

                removeQuantidade(qtde, p); //TIRA QUANTOS TEM

                entradaDao.excluir(Entrada.class, Integer.parseInt(txtCodigo.getText()));

                JOptionPane.showMessageDialog(this, "Excluído com Sucesso.");
                this.dispose();

            } else if (resposta == JOptionPane.NO_OPTION) {
                JOptionPane.showMessageDialog(this, "Operação Cancelada.", null, JOptionPane.INFORMATION_MESSAGE);
            }
        }

        botaoInicial();
        novo = false;
        habilitado(false);
        limparTudo();


    }//GEN-LAST:event_btnExcluirActionPerformed

