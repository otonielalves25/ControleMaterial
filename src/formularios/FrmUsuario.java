    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        // TODO add your handling code here:
        // Validando os campos obrigatórios
        if (ValidarCampos.validarCampo(txtTexto, "nome")) {
            return;
        }

        if (ValidarCampos.validarCampo(txtLogin, "login")) {
            return;
        }

        if (ValidarCampos.validarCampo(cboPrevilegio, "previlégio")) {
            return;
        }

        //  Fim - Validando os campos obrigatórios
        Usuario usuario = new Usuario();
        usuario.setNome(txtTexto.getText().toUpperCase());
        usuario.setLogin(txtLogin.getText());
        usuario.setSenha(senhaVelha);
        usuario.setAtivo(ckAtivo.isSelected());
        int prev = (cboPrevilegio.getSelectedItem().equals("Administrador")) ? 1 : 0;
        usuario.setPrevilegio(prev);
        usuario.setDataCadastro(dataCadastro);

        if (!senhaVelha.equalsIgnoreCase(txtSenha.getText())) {
            usuario.setSenha(ConvertendoSenhaMD5.ConvertendoSenha(txtSenha.getText()));
        }

        // CADATRAO NOVO NO BANCO //////////////////////////////////////////
        if (novo) {
            if (ValidarCampos.validarCampo(txtSenha, "senha")) {
                return;
            }

            usuario.setDataCadastro(new SimpleDateFormat("yyyy-MM-dd").format(new Date()));
            usuarioDao.salvar(usuario);
            JOptionPane.showMessageDialog(this, "Cadatrado com Sucesso !!!", null, JOptionPane.INFORMATION_MESSAGE);

        } else {

            usuario.setIdUsuario(Integer.parseInt(txtCodigo.getText()));
            usuarioDao.update(usuario);
            JOptionPane.showMessageDialog(this, "Alterado com Sucesso !!!", null, JOptionPane.INFORMATION_MESSAGE);

        }
        carregaGrelha();
        botaoInicial();
        novo = false;
        habilitado(false);
        limparTudo();


    }//GEN-LAST:event_btnSalvarActionPerformed


