    //CARREGA GRELHA OTIMIZADA-------------------------------------------------
    private void carregaGrelha() {

        int contador = 0;
        DefaultTableModel modelo = (DefaultTableModel) grelha.getModel();
        modelo.setNumRows(0);

        List<Usuario> lista = usuarioDao.getListagemOrdenado();

        for (Usuario usuario : lista) {

            modelo.addRow(new Object[]{
                usuario.getIdUsuario(),
                usuario.getNome(),
                usuario.getLogin(),
                (usuario.getPrevilegio() == 1) ? "Administrador" : "Consulta",
                (usuario.isAtivo()) ? "Ativo" : "Inativo",});
        }
    }

    //FUNÇÃO QUE BUSCA O USUARIO NO BANCO //////////////////////////////////////
    private void buscarUsuario() {

        int id = (int) grelha.getValueAt(grelha.getSelectedRow(), 0);
        Usuario usuario = usuarioDao.consultaPorID(Usuario.class, id);
        txtCodigo.setText(usuario.getIdUsuario() + "");
        txtTexto.setText(usuario.getNome());
        txtLogin.setText(usuario.getLogin());
        senhaVelha = ConvertendoSenhaMD5.ConvertendoSenha(usuario.getSenha());
        txtSenha.setText(usuario.getSenha());
        if (usuario.getPrevilegio() == 1) {
            cboPrevilegio.setSelectedItem("Administrador");
        } else {
            cboPrevilegio.setSelectedItem("Consulta");
        }
        cboPrevilegio.setSelectedItem(usuario.getPrevilegio());
        ckAtivo.setSelected(usuario.isAtivo());
        dataCadastro = usuario.getDataCadastro();

    }

