    // PESQUISA AVANÇADA DE FUNCIONARIOS ///////////////////////////////////////
    private void carregaPesquisa() {
        String pesquisa = txtPesquisa.getText().trim();
        List<Setor> listagem = setorDao.getListagemOrdenadoLike(pesquisa);
        modeloTabela.setNumRows(0);
        for (Setor s : listagem) {
            modeloTabela.addRow(new Object[]{
                s.getIdSetor(),
                s.getNomeSetor(),
                s.getTipoSetor(),});
        }
    }

