    // CARREGA GATEGORIA ///////////////////////////////////////////////////////
    private void carregaCategoria() {
        List<Categoria> listagem = new CategoriaDao().getListagemOrdenado();
        cboCategoria.addItem("Selecione...");
        for (Categoria categoria : listagem) {
            cboCategoria.addItem(categoria);
        }
    }

    // CARREGA GATEGORIA ///////////////////////////////////////////////////////
    private void carregaUnicadeMedida() {
        List<UnidadeMedida> listagem = new UnidadeMedidaDao().getListagemOrdenado();
        cboUnidade.addItem("Selecione...");
        for (UnidadeMedida unidade : listagem) {
            cboUnidade.addItem(unidade);
        }
    }

