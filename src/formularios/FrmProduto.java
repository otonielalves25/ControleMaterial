    private void btnBuscaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscaActionPerformed
        String caminhaImagem;
        int largura, altura;
        try {
            JFileChooser file = new JFileChooser();
            FileNameExtensionFilter filtro = new FileNameExtensionFilter("Pasta de Seleção aberta.", "jpg", "png", "jpeg");
            file.addChoosableFileFilter(filtro);
            file.setAcceptAllFileFilterUsed(false);
            file.setDialogType(JFileChooser.OPEN_DIALOG);
            //file.showOpenDialog(this);

            if (file.showOpenDialog(null) == JFileChooser.APPROVE_OPTION) {
                caminhaImagem = file.getSelectedFile().getPath();
                largura = paneFoto.getHeight();
                altura = paneFoto.getWidth();
                ImageIcon icon = new ImageIcon(ManipulaImagem.setImagemDimensao(caminhaImagem, largura, altura));
                paneFoto.setIcon(icon);
                paneFoto.setText("");
            }
        } catch (HeadlessException e) {
            paneFoto.setIcon(new ImageIcon());
            paneFoto.setText("IMAGEM");
        }
    }//GEN-LAST:event_btnBuscaActionPerformed

