    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        // TODO add your handling code here:
        // Validando os campos obrigatórios
        if (ValidarCampos.validarCampo(txtNome, "nome material")) {
            return;
        }

        if (ValidarCampos.validarCampo(cboCategoria, "categoria")) {
            return;
        }
        if (ValidarCampos.validarCampo(cboUnidade, "unidade medida")) {
            return;
        }

        if (ValidarCampos.validarCampo(txtQuantidade, "quantidade")) {
            return;
        }

        if (paneFoto.getText().equals("IMAGEM") || paneFoto.getText().equals("SEM IMAGEM")) {
            JOptionPane.showMessageDialog(this, "Adicione uma FOTO.", null, JOptionPane.ERROR_MESSAGE);
            return;
        }
 
        
        //  Fim - Validando os campos obrigatórios
        Produto produto = new Produto();
        produto.setNomeProduto(txtNome.getText().toUpperCase());
        produto.setCategoria((Categoria) cboCategoria.getSelectedItem());
        produto.setUnidadeMedida((UnidadeMedida) cboUnidade.getSelectedItem());
        produto.setDescricao(txtDescricao.getText());
        produto.setObservacao(txtObservacao.getText());
        produto.setFoto(utilidades.ManipulaImagem.convertePanelFotoEmObjeto(paneFoto));
        produto.setQuantidade(Integer.parseInt(txtQuantidade.getText()));
        produto.setQuantidadeMinima(Integer.parseInt(txtQuantidadeMinima.getText()));
        produto.setDataCadastro(new SimpleDateFormat("yyyy-MM-dd").format(new Date())); // CRIA UMA DATA NA HORA

        // CADATRAO NOVO NO BANCO //////////////////////////////////////////
        if (novo) {

            produtoDao.salvar(produto);
            JOptionPane.showMessageDialog(this, "Cadatrado com Sucesso !!!", null, JOptionPane.INFORMATION_MESSAGE);

        } else {

            produto.setIdProduto(Integer.parseInt(txtCodigo.getText()));
            produtoDao.update(produto);
            JOptionPane.showMessageDialog(this, "Alterado com Sucesso !!!", null, JOptionPane.INFORMATION_MESSAGE);

        }

        botaoInicial();
        novo = false;
        habilitado(false);
        limparTudo();
    }//GEN-LAST:event_btnSalvarActionPerformed

