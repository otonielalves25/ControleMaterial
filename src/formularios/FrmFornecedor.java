    //CARREGA GRELHA OTIMIZADA-------------------------------------------------
    private void carregaGrelha() {
     
        DefaultTableModel modelo = (DefaultTableModel) grelha.getModel();
        modelo.setNumRows(0);

        List<Fornecedor> lista = fornecedorDao.getListagemOrdenado(txtPesquisa.getText());       

        for (Fornecedor fornecedor : lista) {

            modelo.addRow(new Object[]{
                fornecedor.getIdFornecedor(),
                fornecedor.getNomeFornecedor(),
                fornecedor.getCidade(),
                fornecedor.getTelefone1(),
                (fornecedor.isAtivo()) ? "Ativo" : "Inativo",});
        }
        
        lblResultado.setText("Total de Fornecedores Cadastrados " +lista.size());
    }

    //FUNÇÃO QUE BUSCA O USUARIO NO BANCO //////////////////////////////////////
    private void buscar() {

        int id = (int) grelha.getValueAt(grelha.getSelectedRow(), 0);
        Fornecedor fornecedor = fornecedorDao.consultaPorID(Fornecedor.class, id);
        txtCodigo.setText(fornecedor.getIdFornecedor() + "");
        txtNome.setText(fornecedor.getNomeFornecedor());
        txtEndereco.setText(fornecedor.getEndereco());
        txtCidade.setText(fornecedor.getCidade());
        txtUF.setText(fornecedor.getUf());
        txtTelefone1.setText("");
        txtTelefone2.setText("");        
        txtTelefone1.setText(fornecedor.getTelefone1());
        txtTelefone2.setText(fornecedor.getTelefone2());
        ckAtivo.setSelected(fornecedor.isAtivo());
        txtObservacao.setText(fornecedor.getObservacao());

    }

